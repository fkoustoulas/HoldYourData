    private boolean idExists(int id){
        TableModel model = jTable1.getModel();
        if(String.valueOf(id).matches("[0-9]*"))
            for(int i = 0; i < model.getRowCount(); i++){
                if(String.valueOf(id).equals(model.getValueAt(i, 0)));
                    return true;
            }
        return false;
    }
