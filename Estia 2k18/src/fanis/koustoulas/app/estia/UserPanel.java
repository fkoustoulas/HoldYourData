    private void insertBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertBtnActionPerformed
        

        //krataei thn epomenh timh apo to teleutaio id pou eisxwrh8hke;
        if(jTable1.getModel().getRowCount() == 0){
            currentID = 0;
        }else{
            currentID = (Integer)jTable1.getModel().getValueAt(jTable1.getModel().getRowCount() - 1, 0) + 1;
        }
        if(!nameText.getText().isEmpty() && !surnameText.getText().isEmpty() && !roomText.getText().isEmpty() && !stateText.getText().isEmpty() && !cityText.getText().isEmpty() && !addressText.getText().isEmpty() && !numberText.getText().isEmpty() && !kinitoText.getText().isEmpty() && !kinitomText.getText().isEmpty() && !kinitopText.getText().isEmpty() && !statheroText.getText().isEmpty() && !fathernameText.getText().isEmpty() && !mothernameText.getText().isEmpty() && !artautoText.getText().isEmpty() && !imEisText.getText().isEmpty() && !eidikotitaText.getText().isEmpty()){
            if(roomText.getText().matches("[0-9]*") && numberText.getText().matches("[0-9]*")){
                db.insert("INSERT INTO STUDENT(ID,SNAME,SLASTNAME,SROOM) VALUES("+currentID+",'"+nameText.getText()+"','"+surnameText.getText()+"',"+Integer.valueOf(roomText.getText())+")");            
                db.insert("INSERT INTO ADDRESS(ID,NOMOS,POLI,DIEUTHINSI,ARITHMOS) VALUES("+currentID+",'"+stateText.getText()+"','"+cityText.getText()+"','"+addressText.getText()+"',"+Integer.valueOf(numberText.getText())+")");            
                db.insert("INSERT INTO GONEIS(ID,ONOMAMITROS,ONOMAPATROS) VALUES("+currentID+",'"+mothernameText.getText()+"','"+fathernameText.getText()+"')");
                db.insert("INSERT INTO TILEFONO(ID,KINITOIDIOU,KINITOPATROS,KINITOMITROS,STATHERO) VALUES("+currentID+",'"+kinitoText.getText()+"','"+kinitomText.getText()+"','"+kinitopText.getText()+"','"+statheroText.getText()+"')");
                db.insert("INSERT INTO STOIXEIA(ID,ARITHMOSTAUTOTITAS,IMEROMINIAEISODOU,IMEROMINIAEKSODOU,EIDIKOTITA) VALUES("+currentID+",'"+artautoText.getText()+"','"+imEisText.getText()+"','"+imeks.getText()+"','"+eidikotitaText.getText()+"')");            

                setTableData();
                clearText();
                JOptionPane.showMessageDialog(null, "Η εισαγωγή ολοκληρώθηκε", "",
                                       JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null, "Υπήρξε πρόβλημα κατά την εισαγωγή", "Error",
                                        JOptionPane.ERROR_MESSAGE);
            }
        }else{
             JOptionPane.showMessageDialog(null, "Υπήρξε πρόβλημα κατά την εισαγωγή", "Error",
                                        JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_insertBtnActionPerformed

