    private ArrayList listUsers(){
        
        ResultSet student = db.query(""
                + "SELECT * FROM STUDENT INNER JOIN ADDRESS ON STUDENT.ID = ADDRESS.ID "
                + "INNER JOIN GONEIS ON STUDENT.ID = GONEIS.ID " 
                + "INNER JOIN TILEFONO ON STUDENT.ID = TILEFONO.ID "
                + "INNER JOIN STOIXEIA ON STUDENT.ID = STOIXEIA.ID ");
        
        Object row[];
        ArrayList<Object> list = new ArrayList();
        try {
            while(student.next()){
                
                row = new Object[18];
                row[0] = student.getInt("ID");
                row[1] = student.getString("SNAME");
                row[2] = student.getString("SLASTNAME");
                row[3] = student.getString("SROOM");
                row[4] = student.getString("NOMOS");
                row[5] = student.getString("POLI");
                row[6] = student.getString("DIEUTHINSI");
                row[7] = student.getInt("ARITHMOS");
                row[8] = student.getString("ONOMAMITROS");
                row[9] = student.getString("ONOMAPATROS");
                row[10] = student.getString("KINITOIDIOU");
                row[11] = student.getString("KINITOPATROS");
                row[12] = student.getString("KINITOMITROS");
                row[13] = student.getString("STATHERO");
                row[14] = student.getString("ARITHMOSTAUTOTITAS");
                row[15] = student.getString("IMEROMINIAEISODOU");
                row[16] = student.getString("IMEROMINIAEKSODOU");
                row[17] = student.getString("EIDIKOTITA");
                list.add(row);
                
            }
            return list;
        } catch (Exception ex) {
            Logger.getLogger(UserPanel.class.getName()).log(Level.SEVERE, null, ex);
            return null;
        }
    }
    
    private void setTableData(){
        ArrayList<Object> list = listUsers();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        model.setRowCount(0);
        for(int i = 0; i < list.size(); i++){
            model.addRow((Object[])list.get(i));
        }
    }

